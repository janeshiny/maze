        // TODO add your handling code here:
        String sql="select * from New_AC where Acc_No=? and Pin=?";
        try{
           pst=conn.prepareStatement(sql);
           pst.setString(1, jTextField1.getText());
           pst.setString(2, jPasswordField1.getText());
           rs=pst.executeQuery();
           if(rs.next()){
               setVisible(false);

                int x=Integer.parseInt(jTextField1.getText());
                MyPage ob=new MyPage(x);
               ob.setVisible(true);
               rs.close();
               pst.close();
           }
           else{
               JOptionPane.showMessageDialog(null, "Incorrect Pin and Account No.");
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        } finally{
            try{           	
               pst.close();
               conn.close();
            }catch(Exception e){    
            }
        }
